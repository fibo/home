alias ',,'='cd .. && pwd'
alias ','='ls -Galrth'

alias ga='git add .'
alias gd='git diff'
alias gc='git commit'
alias gl='git log'
alias gpl='git pull'
alias gps='git push'
alias gpsf='git push --force-with-lease'
alias gs='git status'

alias ns='npm start'
